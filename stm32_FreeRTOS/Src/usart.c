/**
 * @brief       UART数据接收回调接口
                数据处理在这里进行
 * @param       huart:串口句柄
 * @retval      无
 */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
   if (huart->Instance == USART6)                    /* 如果是串口8 */
    {
        if ((g_usart_rx_sta & 0x8000) == 0)             /* 接收未完成 */
        {
            if (g_usart_rx_sta & 0x4000)                /* 接收到了0x0d（即回车键） */
            {
                if (g_rx_buffer[0] != 0x0a)             /* 接收到的不是0x0a（即不是换行键） */
                {
                    g_usart_rx_sta = 0;                 /* 接收错误,重新开始 */
                }
                else                                    /* 接收到的是0x0a（即换行键） */
                {
										//set_spd = atoi((const char*)g_usart_rx_buf);

//										set_spd=g_usart_rx_buf[0]-'0';
//										for(int i=0;i<(g_usart_rx_sta&0x3fff);i++)
//										{
//											set_spd |= ((int32_t)(g_usart_rx_buf[i]-'0')) << (8 * i);
//										}
										g_usart_rx_buf[g_usart_rx_sta & 0x3FFF] = '\0'; // 添加字符串结束符
                    g_usart_rx_sta |= 0x8000;           /* 接收完成了 */
                }
            }
            else                                        /* 还没收到0X0d（即回车键） */
            {
                if (g_rx_buffer[0] == 0x0d)
                    g_usart_rx_sta |= 0x4000;
                else
                {
                    g_usart_rx_buf[g_usart_rx_sta & 0X3FFF] = g_rx_buffer[0];
                    g_usart_rx_sta++;

                    if (g_usart_rx_sta > (USART_REC_LEN - 1))
                    {
                        g_usart_rx_sta = 0;             /* 接收数据错误,重新开始接收 */
                    }
                }
            }
        }

        HAL_UART_Receive_IT(&huart6, (uint8_t *)g_rx_buffer, RXBUFFERSIZE);
    }
		if (huart->Instance == UART7)                    /* 如果是串口8 */
    {
        if ((g_usart_rx_sta2 & 0x8000) == 0)             /* 接收未完成 */
        {
            if (g_usart_rx_sta2 & 0x4000)                /* 接收到了0x0d（即回车键） */
            {
                if (g_rx_buffer2[0] != 0x0a)             /* 接收到的不是0x0a（即不是换行键） */
                {
                    g_usart_rx_sta2 = 0;                 /* 接收错误,重新开始 */
                }
                else                                    /* 接收到的是0x0a（即换行键） */
                {

										g_usart_rx_buf2[g_usart_rx_sta2 & 0x3FFF] = '\0'; // 添加字符串结束符
                    g_usart_rx_sta2 |= 0x8000;           /* 接收完成了 */
                }
            }
            else                                        /* 还没收到0X0d（即回车键） */
            {
                if (g_rx_buffer2[0] == 0x0d)
                    g_usart_rx_sta2 |= 0x4000;
                else
                {
                    g_usart_rx_buf2[g_usart_rx_sta2 & 0X3FFF] = g_rx_buffer2[0];
                    g_usart_rx_sta2++;

                    if (g_usart_rx_sta2 > (USART_REC_LEN - 1))
                    {
                        g_usart_rx_sta2= 0;             /* 接收数据错误,重新开始接收 */
                    }
                }
            }
        }

        HAL_UART_Receive_IT(&huart7, (uint8_t *)g_rx_buffer2, RXBUFFERSIZE);
    }
}

